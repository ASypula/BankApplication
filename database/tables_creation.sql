RETURN VARCHAR2
AS
    v_code VARCHAR2(19);
    v_check NUMBER := 0;
BEGIN
    v_code := F_GENERATE_CODE();
    SELECT count(account_no) INTO v_check FROM bank_accounts WHERE account_no = v_code;
    WHILE v_check <> 0 LOOP
        v_code := F_GENERATE_CODE();
        SELECT count(account_no) INTO v_check FROM bank_accounts WHERE account_no = v_code;
    END LOOP;

    RETURN v_code;
END;

